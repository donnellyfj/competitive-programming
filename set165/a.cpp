#include <bits/stdc++.h>
using namespace std;

// The Ultimate Square

void solve(int n) {
    cout << (n + 1) / 2 << '\n';
}

int main() {
    int t;
    int n;

    // cout << "Input num of test cases: ";
    cin >> t;

    while (t--) {
        // cout << "Input array size: ";
        cin >> n;

        solve(n);
    }

    return 0;
}
